# Variable Names

#  A variable must start with letter or underscore (num cant be added)
#
# A variable only contain alpha-numeric+underscore
#
# A Python keyword cannot be used as variable.


# legal variables:-

My_var=12
var1=23
_var34=11
VAR_=10

print(My_var)
print(var1)
print(_var34)
print(VAR_)

# Illegal Variables:-

# 1var=23
# var-11=9
# %var=11
#
# print(1var)
# print(var-11)
# print(%var)